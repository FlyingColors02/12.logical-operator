//logical operator

/* && operator */
numValue1=5;
numValue2=20;
if(numValue1<10 && numValue1!==0)//true
{
    console.log(true);
}
else{
    console.log(false);
}

if(numValue1>10 && numValue1!=0)//false
{
    console.log(true);
}
else{
    console.log(false);
}
//here both condition should be true.

///////////////////////////////////////////////////////////////////

/* || operator */
if(numValue2===20 || numValue2>60)//true
{
    console.log(true);
}
else{
    console.log(false);
}
//here either one condition should be true.